								<br>
								<br>
								<p>
								Mengapa Obat Paten Lebih Murah?
								<br>
								Merek obat berasal dari perusahaan obat yang pertama kali mematenkannya. 
								Berakhirnya masa berlaku hak paten dilakukan untuk menghilangkan monopoli pada penjualan obat. Dengan hilangnya merek, biaya yang dikeluarkan perusahaan obat bisa ditekan. 
								Apalagi untuk biaya iklan yang paling banyak menyedot dana perusahaan.
								Namun untuk obat generik tidak menggunakan iklan untuk promosinya, oleh karena itu harganya bisa lebih murah dari obat paten.
								</p>
