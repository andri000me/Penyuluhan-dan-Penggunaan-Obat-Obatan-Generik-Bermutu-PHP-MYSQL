								OBAT GENERIK
								<br>
								<br>
								Obat generik adalah obat yang sudah habis masa patennya. 
								<br>
								Obat generik merupakan jenis obat yang memiliki kesamaan kandungan bahan aktif dengan obat paten dalam hal
								kegunaan, formulasi, kekuatan, dosis, kualitas, dan keamanan bagi pemakainya. Meskipun memiliki kesamaan, 
								obat generik dijual dengan harga yang jauh lebih murah 
								dibandingkan obat bermerek atau obat paten. Faktor ini lah yang menyebabkan banyak masyarakat ragu dengan khasiat dan 
