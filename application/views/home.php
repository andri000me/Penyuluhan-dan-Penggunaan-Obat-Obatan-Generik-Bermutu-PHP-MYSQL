								<p>
								Perbandingan Obat Generik dan Obat Paten
								<br>
								Pada deskripsi di atas, diketahui obat generik memiliki kesamaan dalam berbagai hal.
								Namun, memiliki kesamaan bukan berarti seluruh komponen dari obat generik 100% sama dengan obat paten. Berikut yang perlu diketahui tentang obat generik :
								<br>
								1.	Komposisi
								Komposisi dalam obat generik memang tidak sama 100%,namun obat generik wajib menduplikasi bahan aktif yang ada di dalam obat bermerek/obat paten. Yang boleh berbeda hanyalah warna, rasa, dan bahan-bahan tambahan lainnya.
								<br>
								2.	Keamanan
								Faktor keamanan merupakan pertanyaan sebagian besar masyarakat dikarenakan obat generik dijual dengan harga murah. Padalah, harga murah bukan berarti tidak aman. Dilihat dari komposisi obat generik yang menduplikasi obat paten, tentu saja obat generik memiliki tingkat keamanan dan efek sampig yan sama sebagaimana obat paten.
								<br>
								3.	Efektivitas
								Ada anggapan bahwa proses penyerapan tubuh terhadap obat generik memerlukan waktu lebih lama dibandingkan obat paten. Padahal kenyataannya tidak demikian. Obat generik memiliki kekuatan, kemurnian, stabilitas, kualitas, dan cara kerja yang sama sehingga tidak ada perbedaan saat diserap oleh tubuh. Dengan kata lain, obat generik memiliki efektivitas yang sama dengan obat paten.
								<br>
								4.	Pemakaian Mesin Produksi
